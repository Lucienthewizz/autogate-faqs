// SET LANGUAGE (global — called from dropdown)
// ============================================================
function setLang(lang) {
  if (lang === currentLang) return;

  var overlay = document.getElementById('overlay');
  var overlayTitle = document.getElementById('overlay-title');
  var overlaySub = document.getElementById('overlay-sub');

  var lt = loadingTexts[lang] || loadingTexts.en;
  overlayTitle.textContent = lt.title;
  overlaySub.textContent = lt.sub;

  // Show overlay
  overlay.classList.add('show');

  // Update dropdown toggle label
  var ld = langData[lang] || langData.en;
  var flagEl = document.querySelector('.current-flag');
  var nameEl = document.querySelector('.current-lang');
  if (flagEl) flagEl.textContent = ld.flag;
  if (nameEl) nameEl.textContent = ld.name;

  // Update active option in dropdown
  document.querySelectorAll('.lang-option').forEach(function(opt) {
    opt.classList.toggle('active', opt.getAttribute('data-lang') === lang);
  });

  // Close dropdown
  var dropdown = document.getElementById('langDropdown');
  if (dropdown) dropdown.classList.remove('open');

  // Apply translations after short delay for visual feedback
  setTimeout(function() {
    applyTranslations(lang);
    currentLang = lang;

    // Handle RTL for Arabic
    document.documentElement.dir = (lang === 'ar') ? 'rtl' : 'ltr';

    // Hide overlay
    setTimeout(function() {
      overlay.classList.remove('show');
    }, 350);
  }, 600);
}

// ============================================================
// ACCORDION TOGGLE (global — called from onclick)
// ============================================================
function toggleAccordion(btn) {
  var item = btn.closest('.accordion-item');
  var wasOpen = item.classList.contains('open');

  // Close all accordion items
  document.querySelectorAll('.accordion-item').forEach(function(i) {
    i.classList.remove('open');
  });

  // Open clicked one if it wasn't already open
  if (!wasOpen) {
    item.classList.add('open');
  }
}

